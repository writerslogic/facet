// Generate wrangler.test.jsonc from wrangler.jsonc for the vitest-pool-workers runtime.
//
// WHY this file differs from wrangler.jsonc: this miniflare version can't resolve the external AI
// worker, so the `ai` binding crashes the pool at startup. The NL pipeline is tested via an
// injectable stub LlmRunner and /api/stats/query returns 503 when env.AI is absent — exactly this
// case. Rather than hand-maintain a near-duplicate (drift risk), we strip ONLY the `ai` binding
// (plus the post-v1 comment block, which references bindings not present at test time) and write a
// stable, deterministically formatted file. Kept git-tracked so CI reviews the generated output.
//
// Deterministic: parses wrangler.jsonc (JSONC → JSON), deletes `ai`, and pretty-prints with 2-space
// indent and a fixed header. Running it when nothing changed produces an identical byte stream (no
// noisy diff). `pnpm --filter @facet/server pretest` runs this before every test run.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const dir = fileURLToPath(new URL('..', import.meta.url));
const srcPath = `${dir}wrangler.jsonc`;
const outPath = `${dir}wrangler.test.jsonc`;

// Strip // line comments and /* */ block comments (none of ours are inside string literals), then
// drop trailing commas, so JSON.parse accepts the JSONC source.
function stripJsonc(text) {
	const noBlock = text.replace(/\/\*[\s\S]*?\*\//g, '');
	const noLine = noBlock.replace(/(^|[^:])\/\/.*$/gm, '$1');
	return noLine.replace(/,(\s*[}\]])/g, '$1');
}

const config = JSON.parse(stripJsonc(readFileSync(srcPath, 'utf8')));
delete config.ai;

const header = [
	'// GENERATED by scripts/gen-test-wrangler.mjs from wrangler.jsonc — do not edit by hand.',
	'// Test-only Worker config: identical to wrangler.jsonc EXCEPT the `ai` binding is stripped,',
	'// because this miniflare version cannot resolve the external AI worker and crashes at startup.',
	'// The NL pipeline is tested via an injectable stub LlmRunner; /api/stats/query returns 503 when',
	'// env.AI is absent. Regenerate with `pnpm --filter @facet/server pretest`.',
].join('\n');

writeFileSync(outPath, `${header}\n${JSON.stringify(config, null, 2)}\n`);
